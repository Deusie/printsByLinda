<div id="section22" class="container" >
    <h4>Where Did Teespring Come From?</h4>
    <p>Teespring was born on a whim and founded by Walker Williams and Evan Stites-Clayton in 2011 while the two were seniors at Brown University. When Fish Co., their favorite local dive bar (now the Whiskey Republic) was closing, they developed a website in mere hours to sell commemorative shirts reading “Free Fish Co.” They designed the site to accept prepaid orders before printing anything in an effort to bypass heavy upfront production costs.</p>
    <p>Within hours the site collected hundreds of orders. They realized that the concept held great potential beyond their local effort and launched the site in November of 2011 to allow anyone to create a crowdfunding listing for custom apparel. Since then, the company has experienced enormous growth across the globe.</p>
    <p>The Teespring platform allows people to take their own designs, print them on various products, and sell them without overhead costs or restrictions usually associated with starting an online store.</p>
</div>
